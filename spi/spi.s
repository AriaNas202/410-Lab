spiInit:
	PUSH {r4-r12, lr}	; Store register lr on stack

	;!	Enable SSI Module
	;RCGCSSI (pg 346) (400FE61C)
	;(r0-address; r1-data)
	mov r0, #0xE000
	movt r0, #0x400F
	add r0, r0, #0x61C		;get effective address

	MOV r1, #0x4			;enable ssi module 2 (notes say so)

	str r1, [r0]			;update register


	POP {r4-r12, lr}
	MOV pc, lr









