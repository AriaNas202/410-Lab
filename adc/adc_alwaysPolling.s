adcStart:
	PUSH {r4-r12, lr}	; Store register lr on stack


	;Initalize the ADC Unit
	bl ADC_init





;Loops infinitely so that we can pause and check r0's value
infinLoop:

	;Get the current value, returned in r0
	bl poll_ADC

	;SET A BREAKPOINT HERE
	nop

	b infinLoop


	POP {r4-r12, lr}
	MOV pc, lr

